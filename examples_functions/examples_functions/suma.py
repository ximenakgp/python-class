def suma(a, b):
    """
    Esta función suma dos números.

    Parámetros
    ----------
    a : int o float
        Primer número para sumar.
    b : int o float
        Segundo número para sumar.

    Returns
    -------
    int o float
        Suma de a y b.

    Ejemplos
    --------
    >>> suma(4, 5)
    9
    >>> suma(3.5, 6.3)
    9.8
    """
    return a + b



suma()

